            <h1 class = "history">NSBE History</h1>
<p class = "about">
  In 1971, two Purdue undergraduate students, Edward Barnette (now deceased) and Fred Cooper approached the dean of engineering at Purdue University with the concept of starting the Black Society of Engineers (BSE). They wanted to establish a student organization to help improve the recruitment and retention of black engineering students. In the late 1960&#39;s, a devastating 80 percent of the black freshmen entering the engineering program dropped out. The dean agreed to the idea and assigned the only black faculty member on staff, Arthur J. Bond, as advisor.<br>
  <br />
  Barnett served as the first president of the BSE. The fledging group gained momentum in 1974, with the direction and encouragement of Bond and the active participation of the young men whose destiny was to become the founders of NSBE. Now known as the &quot;Chicago Six&quot;, these men are Anthony Harris, Brian Harris, Stanley L. Kirtley, John W. Logan, Jr., Edward A. Coleman, and George A. Smith.</br>
  <br />
  Encouraged by their on-campus success, Anthony Harris, president of the Purdue chapter, wrote a letter to the presidents and deans of every accredited engineering program in the country (288), explained the Society of Black Engineers (SBE) concept and asked them to identify black student leaders, organizations and faculty members who might support their efforts on a national basis. Approximately 80 schools responded. Many had similar Black student organizations with similar objectives. A date was set for the first national meeting and 48 students representing 32 schools attended the event, held April 10-12, 1975. Harris also changed the organizations&#39; nomenclature from the BSE to the Society of Black Engineers (SBE).<br />
  <br />
  It was at that historic meeting through majority vote, that SBE became the National Society of Black Engineers. The familiar NSBE symbol &quot;N&quot; with lightning bolts was chosen and it remains a distinctively recognizable symbol representing the premier technical organization for African American engineering students and professionals. NSBE was eventually incorporated in Texas, in 1976 as 501&copy;3 non-profit organization. John Cason, also of Purdue, served as the first elected president of NSBE. As the organization grew, Virginia Booth became the first female National Chairperson and the first to serve two terms 1978-1980.<br />
  <br/>
  The torch symbolizes members&#39; everlasting, burning desire to achieve success in a competitive society and positively affect the quality of life for all people. The lightening bolt represents the striking impact that will be felt by the society and industry due to the contributions and accomplishments made by the dedicated members of the National Society of Black Engineers.
  NSBE has since grown from six to over 31,000 members and the annual meeting has blossomed into the Annual National Convention, hosting over 8,000 attendees. NSBE has 99 active NSBE Jr. pre-college, 250 student and 68 professional chapters.<br />
  <br />
  Headquartered in Alexandria, Va., NSBE offers academic excellence programs, scholarships, leadership training, professional development and access to career opportunities for thousands of members annually. With over 2000 elected leadership positions, 18 regional conferences and an annual convention, NSBE provides opportunities for success that remain unmatched by any other organization<br />
